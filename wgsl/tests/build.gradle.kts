tasks.register("goldenCoverageReport") {
    group = "verification"
    description = "Generate a markdown coverage report for the WGSL golden corpus"
    mustRunAfter(tasks.named("jvmTest"))

    val goldenRoot = rootProject.layout.projectDirectory.dir("tests/golden")
    val inputsDir = goldenRoot.dir("inputs")
    val outputsDir = goldenRoot.dir("outputs")
    val junitResultsDir = layout.buildDirectory.dir("test-results/jvmTest")
    val reportFile = layout.buildDirectory.file("reports/golden-coverage/summary.md")

    inputs.files(inputsDir)
    inputs.files(outputsDir)
    inputs.files(junitResultsDir).optional()
    outputs.file(reportFile)

    doLast {
        val backendExtensions = linkedMapOf(
            "wgsl" to "wgsl",
            "glsl" to "glsl",
            "hlsl" to "hlsl",
            "msl" to "metal",
            "ir" to "json",
        )
        val inputFiles = inputsDir.asFile.listFilesWithExtension("wgsl")
        val inputStems = inputFiles.mapTo(sortedSetOf()) { it.nameWithoutExtension }
        val report = StringBuilder()

        report.appendLine("# Golden Corpus Coverage")
        report.appendLine()
        report.appendLine("Generated by `:wgsl:tests:goldenCoverageReport`.")
        report.appendLine()
        report.appendLine("## Corpus")
        report.appendLine()
        report.appendLine("- Input files: `${inputFiles.size}`")
        report.appendLine("- Backends: `${backendExtensions.keys.joinToString(", ")}`")
        report.appendLine()
        report.appendLine("| Backend | Expected outputs | Present outputs | Missing outputs | Orphan outputs |")
        report.appendLine("|---------|------------------|-----------------|-----------------|----------------|")

        val backendDetails = backendExtensions.map { (backend, extension) ->
            val backendDir = outputsDir.dir(backend).asFile
            val outputFiles = backendDir.listFilesWithExtension(extension)
            val outputStems = outputFiles.mapTo(sortedSetOf()) { it.nameWithoutExtension }
            val missing = inputStems.minus(outputStems)
            val orphan = outputStems.minus(inputStems)

            report.appendLine("| `$backend` | `${inputFiles.size}` | `${outputFiles.size}` | `${missing.size}` | `${orphan.size}` |")

            GoldenBackendCoverage(
                backend = backend,
                extension = extension,
                missing = missing.toList(),
                orphan = orphan.toList(),
            )
        }

        report.appendLine()
        report.appendLine("## Missing Outputs")
        report.appendLine()
        backendDetails.forEach { detail ->
            report.appendLine("### ${detail.backend}")
            report.appendLine()
            if (detail.missing.isEmpty()) {
                report.appendLine("No missing outputs.")
            } else {
                detail.missing.forEach { stem ->
                    report.appendLine("- `$stem.${detail.extension}`")
                }
            }
            report.appendLine()
        }

        report.appendLine("## Orphan Outputs")
        report.appendLine()
        backendDetails.forEach { detail ->
            report.appendLine("### ${detail.backend}")
            report.appendLine()
            if (detail.orphan.isEmpty()) {
                report.appendLine("No orphan outputs.")
            } else {
                detail.orphan.forEach { stem ->
                    report.appendLine("- `$stem.${detail.extension}`")
                }
            }
            report.appendLine()
        }

        val junitSummary = parseGoldenJUnitResults(junitResultsDir.get().asFile)
        report.appendLine("## JUnit Results")
        report.appendLine()
        if (junitSummary.suites.isEmpty()) {
            report.appendLine("No JUnit XML results found under `wgsl/tests/build/test-results/jvmTest`.")
        } else {
            report.appendLine("| Suite | Tests | Passed | Failures | Errors | Skipped |")
            report.appendLine("|-------|-------|--------|----------|--------|---------|")
            junitSummary.suites.forEach { suite ->
                report.appendLine(
                    "| `${suite.name}` | `${suite.tests}` | `${suite.passed}` | `${suite.failures}` | `${suite.errors}` | `${suite.skipped}` |"
                )
            }
            report.appendLine()
            report.appendLine("### Phase Failure Breakdown")
            report.appendLine()
            if (junitSummary.phaseFailures.isEmpty()) {
                report.appendLine("No `GoldenTestException` phase failures found in existing JUnit XML results.")
            } else {
                report.appendLine("| Backend | Phase | Failures |")
                report.appendLine("|---------|-------|----------|")
                junitSummary.phaseFailures.forEach { (key, count) ->
                    report.appendLine("| `${key.backend}` | `${key.phase}` | `$count` |")
                }
            }
        }
        report.appendLine()

        val outputFile = reportFile.get().asFile
        outputFile.parentFile.mkdirs()
        outputFile.writeText(report.toString())
        logger.lifecycle("Golden coverage report written to: ${outputFile.path}")
    }
}

