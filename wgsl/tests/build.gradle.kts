data class GoldenBackendCoverage(
    val backend: String,
    val extension: String,
    val missing: List<String>,
    val orphan: List<String>,
)

data class GoldenJUnitSummary(
    val suites: List<GoldenJUnitSuite>,
    val phaseFailures: Map<GoldenPhaseFailure, Int>,
)

data class GoldenJUnitSuite(
    val name: String,
    val tests: Int,
    val failures: Int,
    val errors: Int,
    val skipped: Int,
) {
    val passed: Int = tests - failures - errors - skipped
}

data class GoldenPhaseFailure(
    val backend: String,
    val phase: String,
)

fun File.listFilesWithExtension(extension: String): List<File> =
    listFiles()
        ?.filter { it.isFile && it.extension == extension }
        ?.sortedBy { it.name }
        .orEmpty()

fun parseGoldenJUnitResults(resultsDir: File): GoldenJUnitSummary {
    if (!resultsDir.isDirectory) {
        return GoldenJUnitSummary(emptyList(), emptyMap())
    }

    val phasePattern = Regex("\\[([^]]+)] ([a-z-]+) failed for ")
    val suitePattern = Regex("<testsuite\\b([^>]*)>")
    val failurePattern = Regex("<failure\\b[\\s\\S]*?</failure>")
    val phaseFailures = TreeMap<GoldenPhaseFailure, Int>(
        compareBy<GoldenPhaseFailure> { it.backend }.thenBy { it.phase }
    )
    val suites = resultsDir
        .listFilesWithExtension("xml")
        .mapNotNull { xmlFile ->
            runCatching {
                val xml = xmlFile.readText()
                val suiteAttributes = suitePattern.find(xml)?.groupValues?.get(1) ?: return@runCatching null
                failurePattern.findAll(xml).forEach { failure ->
                    phasePattern.find(failure.value)?.let { match ->
                        val key = GoldenPhaseFailure(
                            backend = match.groupValues[1],
                            phase = match.groupValues[2],
                        )
                        phaseFailures[key] = phaseFailures.getOrDefault(key, 0) + 1
                    }
                }
                GoldenJUnitSuite(
                    name = suiteAttributes.getXmlAttribute("name").removeSuffix("[jvm]"),
                    tests = suiteAttributes.getIntXmlAttribute("tests"),
                    failures = suiteAttributes.getIntXmlAttribute("failures"),
                    errors = suiteAttributes.getIntXmlAttribute("errors"),
                    skipped = suiteAttributes.getIntXmlAttribute("skipped"),
                )
            }.getOrNull()
        }
        .sortedBy { it.name }

    return GoldenJUnitSummary(suites, phaseFailures)
}

fun String.getXmlAttribute(name: String): String =
    Regex("""\b$name="([^"]*)"""").find(this)?.groupValues?.get(1).orEmpty()

fun String.getIntXmlAttribute(name: String): Int =
    getXmlAttribute(name).toIntOrNull() ?: 0
